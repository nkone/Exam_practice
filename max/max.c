#include <stdlib.h>

int		max(int *tab, unsigned int len)
{
	int i;
	int hi;

	i = 0;
	hi = tab[i];

	while (i < len)
	{
		if (tab[i] > hi)
			hi = tab[i];
		i++;
	}
	return (hi);
}
